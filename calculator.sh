#!/bin/bash

# (a) Принимаем три аргумента
num1=$1
operator=$2
num2=$3

# Проверяем, что передано три аргумента
if [ $# -ne 3 ]; then
    echo "Ошибка: Неверное количество аргументов"
    echo "Использование: $0 число1 оператор число2"
    echo "Операторы: + - * /"
    exit 1
fi

# Проверяем, что аргументы являются числами
if ! [[ $num1 =~ ^-?[0-9]+$ ]] || ! [[ $num2 =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: Оба аргумента должны быть числами"
    exit 1
fi

# (b) Выполняем операцию и выводим результат
case $operator in
    "+")
        result=$((num1 + num2))
        echo "Результат: $result"
        ;;
    "-")
        result=$((num1 - num2))
        echo "Результат: $result"
        ;;
    "*")
        result=$((num1 * num2))
        echo "Результат: $result"
        ;;
    "/")
        # Проверяем деление на ноль
        if [ $num2 -eq 0 ]; then
            echo "Ошибка: Деление на ноль невозможно"
            exit 1
        fi
        result=$((num1 / num2))
        echo "Результат: $result"
        ;;
    *)
        echo "Ошибка: Неподдерживаемый оператор '$operator'"
        echo "Использование: $0 число1 оператор число2"
        echo "Операторы: + - * /"
        exit 1
        ;;
esac